#Rebando de una Tupla

#Rebanado de la Tupla
#Con numero
Tupla1=tuple('PYTHONHACKINGAPRENDER')

#Removiendo Primer elemento
print("Removiendo primer elemento de la Tupla: ")
print(Tupla1[1:])

#Tupla a la inversa
print("\nTupla despues de la secuencia de elementos a la inversa: ")
print(Tupla1[::-1])

#Elementos de un rango
print("\nImprimiendo elementos de un rango de 4-9: ")
print(Tupla1[4:9])
